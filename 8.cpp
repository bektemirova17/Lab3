#include <iostream>
using namespace std;
	int main () {
	int x, y, z;
	cin >> x >> y >> z;
	if (x > 0) {
	cout << x*x << endl;
	}  else {
	cout << x<< endl;
	} if (y > 0) {
	cout << y*y << endl;
	} else {
	cout << y<< endl;
	} if (z > 0) {
	cout << z*z << endl;
	} else {
	cout << z;
	}
	return 0;
}